# 集合的功能是去重复  集合也不支持下标操作

s1 = {5,8,9,7,2}
print(s1)  # 打印不安孙旭
s2 = {5,8,9,7,5}
print(s2)  # 打印去除了重复
s3 = {'f','d','a'}
print(s3)  # 不按照输入顺序
# 创建空集合 只能使用set()创建
s4 = set()
print(s4)

# 增加数据 add() 增加一个数据 update 增加一个序列
s1 = {10,20}
s1.add(30)
print(s1)
s2 = {40,50}
s1.update(s2)
print(s1)

